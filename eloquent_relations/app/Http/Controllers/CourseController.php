     */
    public function store(Request $request)
    {

        foreach($request->input('enroll_courses') as $enroll){
            $course = new Course();
            $course->enroll_courses = $enroll;
            $course->user_id = Auth::user()->id;
            $course->save();
        }
        return redirect()->back()->with('success','Multiple Data Inserted.  . .');
    }

